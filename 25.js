function f (arg1, arg2) {
    let x = arg1.reduce((a, b) => a + b, 0);
    let y = arg2.reduce((a, b) => a + b, 0);

    if (x >= y){
        return [...arg1, ...arg2];
    }
    else {
        return [...arg2, ...arg1];
    }
    
  };

  
